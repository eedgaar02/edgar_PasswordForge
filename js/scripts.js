function overlayRegister(){
    let registro = document.getElementById("register");
    registro.style.display = "flex";
}
function overlayLogin(){
    let registro = document.getElementById("login");
    registro.style.display = "flex";
}


function hideRegister() {
    let registro = document.getElementById("register").style.display="none";

}
function hideLogin() {
    let registro = document.getElementById("login").style.display="none";

}